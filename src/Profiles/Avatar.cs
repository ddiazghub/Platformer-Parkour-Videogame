using System;

public class Avatar {
    public bool male;
    public int skinColor;
    public int topColor;
    public int bottomColor;

    public Avatar(bool male, int skinColor, int topColor, int bottomColor)
    {
        this.male = male;
        this.skinColor = skinColor;
        this.topColor = topColor;
        this.bottomColor = bottomColor;
    }
}